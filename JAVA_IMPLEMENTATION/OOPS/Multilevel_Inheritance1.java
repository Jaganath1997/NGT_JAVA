package basicPrograms;

/*In this program we can see an example for "Multilevel Inheritance" */

/*Parent class -> Child class -> Grand child class : This hierarchy is Multilevel Inheritance*/

public class Multilevel_Inheritance1 { // Parent class

	/* Let's take animals as an example for this program */

	public void eat() {
		System.out.println("Animals can eat");
	}

	public void run() {
		System.out.println("Animals can run");
	}
}
