package basicPrograms;

/*Inheriting the properties of one class into another class is known as Inheritance. 
There are 5 types of Inheritance. They are :
	1. Single Inheritance
	2. Multilevel Inheritance
	3. Hierarchical Inheritance
	4. Multiple Inheritance
	5. Hybrid Inheritance*/

/*In this program we can see an example for "Single Inheritance" */

/*If one class inherits only one class, it is Single Inheritance*/

public class Single_Inheritance_1 { // Parent class 
	
	 /* Let's take humans and their functionalities as example */
	
	public void head() {
		System.out.println("Humans has 1 head");
	}
	
	public void hands() {
		System.out.println("Humans have 2 hands");
	}
	
	public void legs() {
		System.out.println("Humans have 2 legs");
	}
	
}
